package model;

public class Category {
    String ca_name;
    String ca_filename;
    String ca_deposit;

    public String getCa_name() {
        return ca_name;
    }

    public void setCa_name(String ca_name) {
        this.ca_name = ca_name;
    }

    public String getCa_filename() {
        return ca_filename;
    }

    public void setCa_filename(String ca_filename) {
        this.ca_filename = ca_filename;
    }

    public String getCa_deposit() {
        return ca_deposit;
    }

    public void setCa_deposit(String ca_deposit) {
        this.ca_deposit = ca_deposit;
    }
}